namespace App\Theme\Abstracts;

use Roots\Sage\Container;

use App\Theme\Interfaces\IGenerator as IGenerator;
use App\Theme\Interfaces\IGeneratorFactory as IGeneratorFactory;

/************************************************/
/********** ABSTRACT GENERATOR FACTORY **********/
/************************************************/

abstract class AbstractGeneratorFactory implements IGeneratorFactory
{
    /*******************************/
    /********** ATTRIBUTS **********/
    /*******************************/

    /**
     * @var Container $_container App container
     */

    protected $_container;

    /*********************************************************************************/
    /*********************************************************************************/

    /*******************************/
    /********** CONSTRUCT **********/
    /*******************************/

    public function __construct()
    {
        $this->_setValues();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /********************************/
    /********** SET VALUES **********/
    /********************************/

    protected function _setValues()
    {
        $this->_setContainer();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /***********************************/
    /********** SET CONTAINER **********/
    /***********************************/

    protected function _setContainer()
    {
        $this->_container = Container::getInstance();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /**************************************/
    /********** CREATE GENERATOR **********/
    /**************************************/

    abstract protected function _createGenerator(): IGenerator;

    /*********************************************************************************/
    /*********************************************************************************/

    /****************************/
    /********** CREATE **********/
    /****************************/

    /**
     * @return IGenerator
     */

    public function create(): IGenerator
    {
        return $this->_createGenerator();
    }
}