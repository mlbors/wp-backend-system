namespace App\Theme\Abstracts;

use Roots\Sage\Container;

use App\Theme\Interfaces\IGenerator as IGenerator;

/****************************************/
/********** ABSTRACT GENERATOR **********/
/****************************************/

abstract class AbstractGenerator implements IGenerator
{
    /*******************************/
    /********** ATTRIBUTS **********/
    /*******************************/

    /**
     * @var Mixed $_data input data
     * @var Mixed $_result output data
     */

    protected $_data;
    protected $_result;

    /*********************************************************************************/
    /*********************************************************************************/

    /*******************************/
    /********** CONSTRUCT **********/
    /*******************************/

    public function __construct()
    {
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /******************************/
    /********** SET DATA **********/
    /******************************/

    /**
     * @param Mixed $data input data
     */

    public function setData($data)
    {
        $this->_data = $data;
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /********************************/
    /********** SET RESULT **********/
    /********************************/

    /**
     * @param Mixed $result output data
     */

    protected function _setResult($result)
    {
        $this->_result = $result;
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /******************************/
    /********** GET DATA **********/
    /******************************/

    /**
     * @return Mixed
     */

    public function getData()
    {
        return $this->_data;
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /********************************/
    /********** GET RESULT **********/
    /********************************/

    /**
     * @return Mixed
     */

    public function getResult()
    {
        return $this->_result;
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /******************************/
    /********** GENERATE **********/
    /******************************/

    abstract public function generate();
}