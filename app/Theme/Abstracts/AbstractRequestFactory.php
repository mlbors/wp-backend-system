namespace App\Theme\Abstracts;

use Roots\Sage\Container;

use App\Theme\Interfaces\IRequest as IRequest;
use App\Theme\Interfaces\IRequestFactory as IRequestFactory;

/**********************************************/
/********** ABSTRACT REQUEST FACTORY **********/
/**********************************************/

abstract class AbstractRequestFactory implements IRequestFactory
{
    /*******************************/
    /********** ATTRIBUTS **********/
    /*******************************/

    /**
     * @var Container $_container App container
     */

    protected $_container;

    /*********************************************************************************/
    /*********************************************************************************/

    /*******************************/
    /********** CONSTRUCT **********/
    /*******************************/

    public function __construct()
    {
        $this->_setValues();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /********************************/
    /********** SET VALUES **********/
    /********************************/

    protected function _setValues()
    {
        $this->_setContainer();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /***********************************/
    /********** SET CONTAINER **********/
    /***********************************/

    protected function _setContainer()
    {
        $this->_container = Container::getInstance();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /************************************/
    /********** CREATE REQUEST **********/
    /************************************/

    abstract protected function _createRequest(): IRequest;

    /*********************************************************************************/
    /*********************************************************************************/

    /****************************/
    /********** CREATE **********/
    /****************************/

    /**
     * @return Mixed IRequest
     */

    public function create(): IRequest
    {
        $container = Container::getInstance();
        return $this->_createRequest();
    }
}