namespace App\Theme\Abstracts;

use Roots\Sage\Container;

use App\Theme\Interfaces\ISide as ISide;
use App\Theme\Interfaces\ISideBuilder as ISideBuilder;

/*******************************************/
/********** ABSTRACT SIDE BUILDER **********/
/*******************************************/

abstract class AbstractSideBuilder implements ISideBuilder
{
    /*******************************/
    /********** ATTRIBUTS **********/
    /*******************************/

    /**
     * @var Container $_container App container
     * @var Array $_managerBuilders array of manager builders
     */

    protected $_container;
    protected $_managerBuilders = [];

    /*********************************************************************************/
    /*********************************************************************************/

    /*******************************/
    /********** CONSTRUCT **********/
    /*******************************/

    public function __construct()
    {
        $this->_setValues();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /********************************/
    /********** SET VALUES **********/
    /********************************/

    protected function _setValues()
    {
        $this->_setContainer();
        $this->_setManagerBuilders();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /***********************************/
    /********** SET CONTAINER **********/
    /***********************************/

    protected function _setContainer()
    {
        $this->_container = Container::getInstance();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /******************************************/
    /********** SET MANAGER BUILDERS **********/
    /******************************************/

    abstract protected function _setManagerBuilders();

    /*********************************************************************************/
    /*********************************************************************************/

    /*********************************/
    /********** CREATE SIDE **********/
    /*********************************/

    abstract protected function _createSide(): ISide;

    /*********************************************************************************/
    /*********************************************************************************/

    /****************************/
    /********** CREATE **********/
    /****************************/

    /**
     * @return Mixed ISide
     */

    public function create(): ISide 
    {
        return $this->_createSide();
    }
}