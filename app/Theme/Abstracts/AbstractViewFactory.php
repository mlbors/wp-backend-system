namespace App\Theme\Abstracts;

use Roots\Sage\Container;

use App\Theme\Interfaces\IViewFactory as IViewFactory;
use App\Theme\Interfaces\IView as IView;

/*******************************************/
/********** ABSTRACT VIEW FACTORY **********/
/*******************************************/

abstract class AbstractViewFactory implements IViewFactory
{
    /*******************************/
    /********** ATTRIBUTS **********/
    /*******************************/

    /**
     * @var Container $_container App container
     */

    protected $_container;

    /*********************************************************************************/
    /*********************************************************************************/

    /*******************************/
    /********** CONSTRUCT **********/
    /*******************************/

    public function __construct()
    {
        $this->_setValues();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /********************************/
    /********** SET VALUES **********/
    /********************************/

    protected function _setValues()
    {
        $this->_setContainer();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /***********************************/
    /********** SET CONTAINER **********/
    /***********************************/

    protected function _setContainer()
    {
        $this->_container = Container::getInstance();
    }

    /*********************************************************************************/
    /*********************************************************************************/

    /*********************************/
    /********** CREATE VIEW **********/
    /*********************************/

    abstract protected function _createView(): IView;

    /*********************************************************************************/
    /*********************************************************************************/

    /****************************/
    /********** CREATE **********/
    /****************************/

    /**
     * @return IView
     */

    public function create(): IView
    {
        return $this->_createView();
    }
}